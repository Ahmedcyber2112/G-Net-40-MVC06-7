using System.Diagnostics;
using GymManagementSystem.BLL.Services.Interfaces;
using GymManagementSystem.PL.Models;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Mvc;

namespace GymManagementSystem.PL.Controllers
{
    [Authorize] // ????? ?????? ???????? ???? ?? ????? ??? ???????? ??????
    public class HomeController : Controller
    {
        private readonly ILogger<HomeController> _logger;
        private readonly IMemberService _memberService;
        private readonly ITrainerService _trainerService;
        private readonly ISessionService _sessionService;

        // ??? ??????? ???????? ???? ?????????? ????????
        public HomeController(
            ILogger<HomeController> logger,
            IMemberService memberService,
            ITrainerService trainerService,
            ISessionService sessionService)
        {
            _logger = logger;
            _memberService = memberService;
            _trainerService = trainerService;
            _sessionService = sessionService;
        }

        public async Task<IActionResult> Index()
        {
            // ??? ???????? ?????????? ?? ????? ???????? (???? ?? ?????? ????? ??????? ???? ?? ??? Services)
            var allMembers = await _memberService.GetALLMembersAsync();
            var allTrainers = await _trainerService.GetAllTrainersAsync();
            var allSessions = await _sessionService.GetAllSessionsAsync();

            // ???? ??????? ???????? ??? ??? ViewBag
            ViewBag.TotalMembers = allMembers?.Count() ?? 0;
            ViewBag.ActiveMembers = allMembers?.Count() ?? 0; // ?????? ???? ??? IsActive ????? ???????
            ViewBag.TotalTrainers = allTrainers?.Count() ?? 0;

            // ???????? ??????? (????? ??? ?????? ??? ?????? Status ?? ??? Session)
            ViewBag.UpcomingSessions = allSessions?.Count(s => s.Status == "Upcoming") ?? 0;
            ViewBag.OngoingSessions = allSessions?.Count(s => s.Status == "Ongoing") ?? 0;
            ViewBag.CompletedSessions = allSessions?.Count(s => s.Status == "Completed") ?? 0;

            return View();
        }

        public IActionResult Privacy()
        {
            return View();
        }

        [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
        public IActionResult Error()
        {
            return View(new ErrorViewModel { RequestId = Activity.Current?.Id ?? HttpContext.TraceIdentifier });
        }
    }
}